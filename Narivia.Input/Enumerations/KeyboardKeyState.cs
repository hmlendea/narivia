namespace Narivia.Input.Enumerations
{
    /// <summary>
    /// Keyboard key state.
    /// </summary>
    public enum KeyboardKeyState
    {
        /// <summary>
        /// The key was just pressed.
        /// </summary>
        Pressed,

        /// <summary>
        /// They key was just released.
        /// </summary>
        Released,

        /// <summary>
        /// The key is down.
        /// </summary>
        Down,

        /// <summary>
        /// The key is up.
        /// </summary>
        Up
    }
}
